#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"

#define PCn_2_IDX 19
#define P2_IDX 2
#define PCn_1_IDX 11
#define P1_IDX 1

// ERU (External Request Unit) for S3
#define EXIS0_IDX 4
#define FEN0_IDX 8
#define REN0_IDX 9
#define EIEN0_IDX 11
#define INP0_IDX 12
#define IGP0_IDX 14

// SRC (Service Request Control) for S3
#define SRE_IDX 10
#define TOS_IDX 11


IfxCpu_syncEvent cpuSyncEvent = 0;

void initGPIO(void);
void initERU(void);
void initLED(void);
void blinking(void);

volatile uint8 buttonRequest = 0;

typedef enum {
    STATE_BLINKING,
    STATE_IDLE
} systemState;
systemState currentState = STATE_BLINKING;


IFX_INTERRUPT(ISR0, 0, 0x10);
void ISR0(void)
{
    buttonRequest = 1;
}



int core0_main(void)
{
    IfxCpu_enableInterrupts();

    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());

    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&cpuSyncEvent);
    IfxCpu_waitEvent(&cpuSyncEvent, 1);

    initGPIO();
    initLED();
    initERU();

    while(1)
    {
        if (buttonRequest == 1)
        {
            if (currentState == STATE_BLINKING)
            {
                currentState = STATE_IDLE;
            }
            else
            {
                currentState = STATE_BLINKING;
            }
            buttonRequest = 0;
        }

        switch (currentState)
        {
            case STATE_BLINKING:
                blinking();
                for(volatile int i=0; i<10000000; i++);
                break;

            case STATE_IDLE:
                break;

            default:
                currentState = STATE_IDLE;
                buttonRequest = 0;
                break;
        }
    }

    return (1);
}

void initLED(void)
{
    P10_OMR.U = (1 << (P2_IDX + 16));
    P10_OMR.U = (1 << P1_IDX);
}

void initGPIO(void)
{
    P02_IOCR0.U &= ~(0x1F << PCn_1_IDX);
    P02_IOCR0.U |= 0x02 << PCn_1_IDX;

    P10_IOCR0.U &= ~(0x1F << PCn_2_IDX);
    P10_IOCR0.U |= 0x10 << PCn_2_IDX;

    P10_IOCR0.U &= ~(0x1F << PCn_1_IDX);
    P10_IOCR0.U |= 0x10 << PCn_1_IDX;
}

void initERU(void)
{
    // ERU (External Request Unit) for S3
    SCU_EICR1.U &= ~(0x7 << EXIS0_IDX);
    SCU_EICR1.U |= 0x1 << EXIS0_IDX;

    SCU_EICR1.U &= ~(0x1 << REN0_IDX);
    SCU_EICR1.U |= 0x1 << FEN0_IDX;

    SCU_EICR1.U |= 0x1 << EIEN0_IDX;

    SCU_EICR1.U &= ~(0x7 << INP0_IDX);

    SCU_IGCR0.U &= ~(0x3 << IGP0_IDX);
    SCU_IGCR0.U |= 0x1 << IGP0_IDX;

    // SRC (Service Request Control) for S3
    SRC_SCU_SCU_ERU0.U &= ~0xFF;
    SRC_SCU_SCU_ERU0.U |= 0x10;

    SRC_SCU_SCU_ERU0.U |= 0x1 << SRE_IDX;
    SRC_SCU_SCU_ERU0.U &= ~(0x3 << TOS_IDX);
}

void blinking(void)
{
    if ((P10_OUT.U & (0x1 << P2_IDX)) != 0)
    {
        P10_OMR.U = (1 << (P2_IDX + 16));
    }
    else
    {
        P10_OMR.U = (1 << P2_IDX);
    }

    if ((P10_OUT.U & (0x1 << P1_IDX)) != 0)
    {
        P10_OMR.U = (1 << (P1_IDX + 16));
    }
    else
    {
        P10_OMR.U = (1 << P1_IDX);
    }
}
